package com.mini.cbse.Train;

public class TrainSchedule {
	private int train_no;
	private String from;
	private String from_arr;
	private String from_dept;
	private String to;
	private String to_arr;
	private String to_dept;
	public int getTrain_no() {
		return train_no;
	}
	public void setTrain_no(int train_no) {
		this.train_no = train_no;
	}
	public String getFrom() {
		return from;
	}
	public void setFrom(String from) {
		this.from = from;
	}
	public String getFrom_arr() {
		return from_arr;
	}
	public void setFrom_arr(String from_arr) {
		this.from_arr = from_arr;
	}
	public String getFrom_dept() {
		return from_dept;
	}
	public void setFrom_dept(String from_dept) {
		this.from_dept = from_dept;
	}
	public String getTo() {
		return to;
	}
	public void setTo(String to) {
		this.to = to;
	}
	public String getTo_arr() {
		return to_arr;
	}
	public void setTo_arr(String to_arr) {
		this.to_arr = to_arr;
	}
	public String getTo_dept() {
		return to_dept;
	}
	public void setTo_dept(String to_dept) {
		this.to_dept = to_dept;
	}	
	
}
